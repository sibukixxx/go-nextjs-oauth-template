import { Outlet, Link, useLocation } from 'react-router-dom';
import { Avatar, cn } from '@app/ui';
import { useAuthStore } from '@/stores/authStore';

const navItems = [
  { path: '/', label: 'Dashboard', icon: HomeIcon },
  { path: '/settings', label: 'Settings', icon: SettingsIcon },
];

export function Layout() {
  const location = useLocation();
  const { user, logout } = useAuthStore();

  return (
    <div className="min-h-screen bg-background">
      {/* Header */}
      <header className="sticky top-0 z-50 bg-background-secondary border-b border-border">
        <div className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8">
          <div className="flex items-center justify-between h-16">
            {/* Logo */}
            <Link to="/" className="flex items-center gap-2">
              <div className="w-8 h-8 bg-primary rounded-lg flex items-center justify-center">
                <span className="text-white font-bold">A</span>
              </div>
              <span className="font-display font-semibold text-foreground">
                App
              </span>
            </Link>

            {/* Navigation */}
            <nav className="hidden md:flex items-center gap-1">
              {navItems.map((item) => {
                const isActive = location.pathname === item.path;
                const Icon = item.icon;
                return (
                  <Link
                    key={item.path}
                    to={item.path}
                    className={cn(
                      'flex items-center gap-2 px-4 py-2 rounded-lg text-sm font-medium transition-colors',
                      isActive
                        ? 'bg-primary/10 text-primary'
                        : 'text-foreground-secondary hover:text-foreground hover:bg-background-tertiary'
                    )}
                  >
                    <Icon className="w-4 h-4" />
                    {item.label}
                  </Link>
                );
              })}
            </nav>

            {/* User menu */}
            <div className="flex items-center gap-4">
              <div className="flex items-center gap-3">
                <Avatar
                  src={user?.avatar_url}
                  name={user?.display_name}
                  size="sm"
                />
                <div className="hidden sm:block">
                  <p className="text-sm font-medium text-foreground">
                    {user?.display_name}
                  </p>
                  <p className="text-xs text-foreground-secondary">
                    {user?.primary_email}
                  </p>
                </div>
              </div>
              <button
                onClick={() => logout()}
                className="p-2 text-foreground-secondary hover:text-foreground rounded-lg hover:bg-background-tertiary transition-colors"
                title="Logout"
              >
                <LogoutIcon className="w-5 h-5" />
              </button>
            </div>
          </div>
        </div>
      </header>

      {/* Main content */}
      <main className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8 py-8">
        <Outlet />
      </main>
    </div>
  );
}

